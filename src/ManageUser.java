    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        DefaultTableModel model=(DefaultTableModel) tableUser.getModel();
        try{
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from appuser where userRole='Admin' ");
            while(rs.next()){
                model.addRow(new Object[]{rs.getInt("appuser_pk"),rs.getString("userRole"),rs.getString("name"),rs.getString("mobileNo"),rs.getString("username"),rs.getString("password"),rs.getString("address"),rs.getString("status")});
                appuserPk=rs.getInt("appuser_pk");
            }
            con.close();
            st.close();
        }
         catch(Exception e){
                JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_formComponentShown

