    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String name=txtName.getText();
        String mobileNo=txtMobileNo.getText();
        String username=txtUsername.getText();
        String password=txtPassword.getText();
        String address=txtAddress.getText();
        String status=(String)jComboBoxStatus.getSelectedItem();
        if(validateFields("new")){
            JOptionPane.showMessageDialog(null,"All fields are required!");
        }
        else{
            try{
                Connection con=ConnectionProvider.getCon();
                PreparedStatement ps=con.prepareStatement("insert into appuser (userRole,name, mobileNo,username,password,address,status)values('Admin',?,?,?,?,?,?)");
                ps.setString(1,name);
                ps.setString(2,mobileNo);
                ps.setString(3,username);
                ps.setString(4,password);
                ps.setString(5,address);
                ps.setString(6,status);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null,"User added successfully!");
                setVisible(false);
                new ManageUser().setVisible(true);
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String name=txtName.getText();
        String mobileNo=txtMobileNo.getText();
        String username=txtUsername.getText();
        String address=txtAddress.getText();
        String status=(String)jComboBoxStatus.getSelectedItem();
        if(validateFields("edit")){
            JOptionPane.showMessageDialog(null,"All fields are required!!!");
        }
        else{
            try{
                Connection con=ConnectionProvider.getCon();
                PreparedStatement ps=con.prepareStatement("update appuser set name=?,mobileNo=?,username=?,address=?,status=? where appuser_pk=?");
                ps.setString(1,name);
                ps.setString(2,mobileNo);
                ps.setString(3,username);
                ps.setString(4,address);
                ps.setString(5,status);
                ps.setInt(6,appuserPk);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null,"User updated successfully!");
                con.close();
                ps.close();
                setVisible(false);
                new ManageUser().setVisible(true);
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

